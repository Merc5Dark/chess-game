//Coloring
function coloring() {
    const boxes = document.querySelectorAll('.box');
  
    boxes.forEach((box) => {
      const id = box.id;
      const row = parseInt(id.substring(1, 2));
      const col = parseInt(id.substring(2, 3));
  
      if ((row + col) % 2 === 0) {
        box.style.backgroundColor = '#f2f2f2';
        box.style.color = 'black';
      } else {
        box.style.backgroundColor = '#d9d9d9';
        box.style.color = 'black';
      }
    });
}
coloring();

//function to not remove the same team element
function reddish() {
    document.querySelectorAll('.box').forEach((i1) => {
      if (i1.style.backgroundColor === 'pink') {
        document.querySelectorAll('.box').forEach((i2) => {
          if (
            i2.style.backgroundColor === 'green' &&
            i2.innerText.length !== 0
          ) {
            const greenText = i2.innerText;
            const pinkText = i1.innerText;
  
            const pinkColor = pinkText[0];
            const greenColor = greenText[0];
  
            const getId = i2.id;
            const arr = Array.from(getId);
            arr.shift();
            const aside = eval(arr.pop());
            const aup = eval(arr.shift());
            const a = aside + aup;
  
            if (a % 2 === 0 && pinkColor === greenColor) {
              i2.style.backgroundColor = '#f2f2f2';
              i2.style.color = 'black';
            }
            if (a % 2 !== 0 && pinkColor === greenColor) {
              i2.style.backgroundColor = '#d9d9d9';
              i2.style.color = 'black';
            }
          }
        });
      }
    });
}
  
